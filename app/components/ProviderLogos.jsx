/**
 * ProviderLogo
 * Resolves a provider key to the matching SVG logo.
 *
 * Props:
 *   providerKey {string} — "aws" | "azure" | "gcp" | "onprem"
 *   size        {number}
 *   color       {string}
 *   accent      {string} — used by OnPremIcon dot color
 */
export function ProviderLogo({ providerKey, size, color, accent }) {
  switch (providerKey) {
    case "aws":    return <AWSLogo    size={size} color={color} />;
    case "azure":  return <AzureLogo  size={size} color={color} />;
    case "gcp":    return <GCPLogo    size={size} color={color} />;
    case "onprem": return <OnPremIcon size={size} color={color} accent={accent} />;
    default:       return null;
  }
}